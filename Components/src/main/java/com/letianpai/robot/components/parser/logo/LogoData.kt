package com.letianpai.robot.components.parser.logo

class LogoData {
    var hello_logo: String? = null

    var desktop_logo: String? = null

    override fun toString(): String {
        return "LogoData{" +
                "hello_logo='" + hello_logo + '\'' +
                ", desktop_logo='" + desktop_logo + '\'' +
                '}'
    }
}
